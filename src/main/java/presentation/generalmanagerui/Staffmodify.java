    private void saveMouseClicked(java.awt.event.MouseEvent evt) throws RemoteException {//GEN-FIRST:event_saveMouseClicked
        // TODO add your handling code here:
    	String role="";
    	if(courier.isSelected())
    		role="快递员";
    	if(driver.isSelected())
    		role="司机";
    	if(bushall.isSelected())
    		role="营业厅业务员";
    	if(zhongzhuan.isSelected())
    		role="中转中心业务员";
    	if(stockman.isSelected())
    		role="仓库管理员";
    	if(financialman.isSelected())
    		role="财务人员";
    	if(administrater.isSelected())
    		role="管理员";
    	if(manager.isSelected())
    		role="总经理";
    	sims.modifyStaff(find,new Staffvo(name.getText(),ID.getText(),worktime.getText(),workunit.getText(),role));
    }//GEN-LAST:event_saveMouseClicked

